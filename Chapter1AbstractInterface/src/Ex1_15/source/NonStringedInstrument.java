public class NonStringedInstrument extends Instrument {

	private String use;

	public NonStringedInstrument(String name, String proUnit, String use) {
		super(name, proUnit);
		this.use = use;
	}

	public String getUse() {
		return use;
	}

	public void setUse(String use) {
		this.use = use;
	}
	/*
	 * (non-Javadoc)
	 * @see Ex1_15.source.Instrument#play()
	 * function: use to create instructions on how to play musical instruments
	 * input is use fields
	 * output is information of use
	 */
	@Override
	public void play() {
		System.out.println("\tPlay: " + getUse());
	}

}
